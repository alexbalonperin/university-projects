package model;

/*Cette interface implemente le pattern observer associe aux formes (queue, balles, trous,..).
L'implementation de ses fonctions permettra de mettre a jour les objets de la vue qui observent une forme en leur transmettant les nouvelles valeurs des parametres.
*/

public interface ObserverShape {
	public void update(Shape s);
	public void updateShapeBall(Shape s);
	public void updateDrawingBall();
	public void initialize(Shape s);
}
